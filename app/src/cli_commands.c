void CLI_install_commands(void){
	FreeRTOS_CLIRegisterCommand( &xTasksCommand );
	FreeRTOS_CLIRegisterCommand( &xEchoCommand );
	FreeRTOS_CLIRegisterCommand( &xColorCommand );
}


